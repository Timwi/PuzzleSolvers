namespace PuzzleSolvers;

/// <summary>
///     Represents an orthogonal direction in a 2D puzzle.</summary>
/// <remarks>
///     This enum is used to represent directional clues such as those in <see cref="CastleWall"/>.</remarks>
public enum Direction
{
    /// <summary>Up.</summary>
    Up,
    /// <summary>Right.</summary>
    Right,
    /// <summary>Down.</summary>
    Down,
    /// <summary>Left.</summary>
    Left
}
